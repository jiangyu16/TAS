package com.tas.bean;

public class Paper {
    
    int paperId;
    String paperName;
    int programScore;
    int choiceScore;
    String teacherId;
    int paperType;
    Boolean isVisable;
    int courseId;
    
    /**
     * @return the paperId
     */
    public int getPaperId() {
        return paperId;
    }
    /**
     * @param paperId the paperId to set
     */
    public void setPaperId(int paperId) {
        this.paperId = paperId;
    }
    /**
     * @return the paperName
     */
    public String getPaperName() {
        return paperName;
    }
    /**
     * @param paperName the paperName to set
     */
    public void setPaperName(String paperName) {
        this.paperName = paperName;
    }
    /**
     * @return the programScore
     */
    public int getProgramScore() {
        return programScore;
    }
    /**
     * @param programScore the programScore to set
     */
    public void setProgramScore(int programScore) {
        this.programScore = programScore;
    }
    /**
     * @return the choiceScore
     */
    public int getChoiceScore() {
        return choiceScore;
    }
    /**
     * @param choiceScore the choiceScore to set
     */
    public void setChoiceScore(int choiceScore) {
        this.choiceScore = choiceScore;
    }
    /**
     * @return the teacherId
     */
    public String getTeacherId() {
        return teacherId;
    }
    /**
     * @param teacherId the teacherId to set
     */
    public void setTeacherId(String teacherId) {
        this.teacherId = teacherId;
    }
    /**
     * @return the paperType
     */
    public int getPaperType() {
        return paperType;
    }
    /**
     * @param paperType the paperType to set
     */
    public void setPaperType(int paperType) {
        this.paperType = paperType;
    }
    /**
     * @return the isVisable
     */
    public Boolean getIsVisable() {
        return isVisable;
    }
    /**
     * @param isVisable the isVisable to set
     */
    public void setIsVisable(Boolean isVisable) {
        this.isVisable = isVisable;
    }
    /**
     * @return the courseId
     */
    public int getCourseId() {
        return courseId;
    }
    /**
     * @param courseId the courseId to set
     */
    public void setCourseId(int courseId) {
        this.courseId = courseId;
    }
    
    

}
